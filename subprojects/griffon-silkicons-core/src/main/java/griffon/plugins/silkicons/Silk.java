    @Nonnull
    public String getDescription() {
        return description;
    }

    @Nonnull
    public String asResource() {
        return "com/famfamfam/silk/" + description + ".png";
    }

    @Nonnull
    public static Silk findByDescription(@Nonnull String description) {
        if (isBlank(description)) {
            throw new IllegalArgumentException("Description " + description + " is not a valid Silk icon description");
        }

        for (Silk silk : values()) {
            if (silk.description.equalsIgnoreCase(description)) {
                return silk;
            }
        }

        throw new IllegalArgumentException("Description " + description + " is not a a valid Silk icon description");
    }
